#!/usr/bin/env python3
"""Create a planned graph-candidate experiment scaffold."""

from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path
from typing import Any

REPO_ROOT = Path(__file__).resolve().parents[2]
if str(REPO_ROOT) not in sys.path:
    sys.path.insert(0, str(REPO_ROOT))

from src.kg_pipeline.registry.candidate_registry import (  # noqa: E402
    candidate_by_id,
    load_registry,
    sha256_file,
)

DEFAULT_ALLOCATION = Path("src/Pruning graph/bidirectional_allocation_results5k.json")
DEFAULT_REGISTRY = Path("artifacts/final_graph/selected_final_graph/rebuild/candidate_registry.v1.json")
DEFAULT_OUT_ROOT = Path("experiments/graph_candidates")
MANIFEST_SCHEMA_VERSION = "kg-candidate-experiment-manifest-template-v1"


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--candidate-id", required=True)
    parser.add_argument("--parent-candidate-id", default=None)
    parser.add_argument("--label", required=True)
    parser.add_argument("--strategy", required=True)
    parser.add_argument("--out-root", type=Path, default=DEFAULT_OUT_ROOT)
    parser.add_argument("--allocation", type=Path, default=DEFAULT_ALLOCATION)
    parser.add_argument("--parent-graph", type=Path, default=None)
    parser.add_argument("--registry", type=Path, default=DEFAULT_REGISTRY)
    parser.add_argument("--force", action="store_true")
    return parser.parse_args()


def repo_relative(path: Path) -> str:
    try:
        return str(path.relative_to(REPO_ROOT))
    except ValueError:
        return str(path)


def resolve_path(path: Path) -> Path:
    if path.is_absolute():
        return path
    return REPO_ROOT / path


def refuse_overwrite(paths: list[Path], force: bool) -> None:
    existing = [path for path in paths if path.exists()]
    if existing and not force:
        names = ", ".join(str(path) for path in existing)
        raise FileExistsError(f"Refusing to overwrite existing scaffold files without --force: {names}")


def load_registry_if_available(path: Path | None) -> dict[str, Any] | None:
    if path is None:
        return None
    resolved = resolve_path(path)
    if not resolved.is_file():
        return None
    return load_registry(resolved)


def parent_from_registry(
    registry: dict[str, Any] | None,
    parent_candidate_id: str | None,
) -> dict[str, Any] | None:
    if registry is None or parent_candidate_id is None:
        return None
    return candidate_by_id(registry, parent_candidate_id)


def resolve_parent_graph(
    explicit_parent_graph: Path | None,
    parent_row: dict[str, Any] | None,
) -> tuple[str | None, str | None, list[str]]:
    notes: list[str] = []
    if explicit_parent_graph is not None:
        resolved = resolve_path(explicit_parent_graph)
        if not resolved.is_file():
            raise FileNotFoundError(resolved)
        return repo_relative(explicit_parent_graph), sha256_file(resolved), notes

    if parent_row and parent_row.get("graph_path"):
        parent_graph = Path(parent_row["graph_path"])
        resolved = resolve_path(parent_graph)
        if not resolved.is_file():
            notes.append(
                f"Parent candidate {parent_row.get('candidate_id')} has graph_path but file was not found: {parent_row.get('graph_path')}"
            )
            return str(parent_row["graph_path"]), None, notes
        return str(parent_row["graph_path"]), sha256_file(resolved), notes

    notes.append("No parent graph path was provided or resolved from the registry.")
    return None, None, notes


def intended_outputs() -> dict[str, str]:
    return {
        "candidate_graph": "outputs/graph.jsonl",
        "report": "reports/report.json",
        "summary": "reports/summary.md",
        "relation_quota_report": "reports/relation_quota_report.tsv",
        "pattern_balance_report": "reports/pattern_balance_report.tsv",
        "evaluation_manifest": "reports/manifest.json",
        "decision": "decision.md",
    }


def build_manifest(
    args: argparse.Namespace,
    allocation_path: Path,
    allocation_sha256: str,
    registry_path: Path | None,
    parent_row: dict[str, Any] | None,
    parent_graph_path: str | None,
    parent_graph_sha256: str | None,
    parent_notes: list[str],
) -> dict[str, Any]:
    notes = [
        "No graph was generated by this scaffold.",
        "Run evaluator after graph output exists.",
        "Update registry only after human decision.",
    ]
    notes.extend(parent_notes)
    return {
        "schema_version": MANIFEST_SCHEMA_VERSION,
        "candidate_id": args.candidate_id,
        "label": args.label,
        "strategy": args.strategy,
        "parent_candidate_id": args.parent_candidate_id,
        "parent_graph_path": parent_graph_path,
        "parent_graph_sha256": parent_graph_sha256,
        "allocation_path": repo_relative(args.allocation),
        "allocation_sha256": allocation_sha256,
        "registry_path": repo_relative(registry_path) if registry_path is not None else None,
        "parent_registry_metadata": parent_row,
        "intended_outputs": intended_outputs(),
        "status": "planned_not_generated",
        "notes": notes,
    }


def readme_text(args: argparse.Namespace, manifest: dict[str, Any]) -> str:
    graph_path = manifest["intended_outputs"]["candidate_graph"]
    allocation_path = manifest["allocation_path"]
    if not Path(allocation_path).is_absolute():
        allocation_path = "../../" + allocation_path
    eval_command = (
        "python ../../scripts/graph_candidates/evaluate_candidate.py "
        f"--candidate-id {args.candidate_id} "
        f"--label \"{args.label}\" "
        f"--graph \"{graph_path}\" "
        f"--allocation \"{allocation_path}\" "
        "--out-dir reports --force"
    )
    if args.parent_candidate_id:
        eval_command += f" --parent-candidate-id {args.parent_candidate_id}"

    return "\n".join(
        [
            f"# {args.candidate_id}",
            "",
            f"Label: {args.label}",
            "",
            f"Strategy: `{args.strategy}`",
            "",
            "Status: `planned_not_generated`",
            "",
            "This scaffold does not generate a graph. It only creates the standard directory layout and metadata template for a future graph-candidate experiment.",
            "",
            "## Directory Layout",
            "",
            "- `outputs/`: place the future candidate graph here, usually `outputs/graph.jsonl`.",
            "- `reports/`: evaluator report package output.",
            "- `logs/`: command logs for future generation/evaluation runs.",
            "- `configs/`: run-specific config files.",
            "- `manifest.template.json`: planned candidate metadata.",
            "- `decision.md`: write after evaluation and human review.",
            "",
            "## Evaluation Command After Graph Exists",
            "",
            "Run from this experiment directory after `outputs/graph.jsonl` exists. Adjust the relative script path if the experiment root is nested differently from `experiments/graph_candidates/<candidate-id>/`:",
            "",
            "```bash",
            eval_command,
            "```",
            "",
            "## Decision Step",
            "",
            "After evaluation, write `decision.md` with the candidate status, accepted/rejected decision, metrics, limitations, and evidence paths.",
            "",
            "## Registry Step",
            "",
            "Do not update the candidate registry until a graph exists, reports exist, and a human decision has been recorded.",
            "",
        ]
    )


def main() -> int:
    args = parse_args()
    candidate_root = resolve_path(args.out_root) / args.candidate_id
    dirs = [
        candidate_root,
        candidate_root / "outputs",
        candidate_root / "reports",
        candidate_root / "logs",
        candidate_root / "configs",
    ]
    manifest_path = candidate_root / "manifest.template.json"
    readme_path = candidate_root / "README.md"
    refuse_overwrite([manifest_path, readme_path], args.force)

    allocation_path = resolve_path(args.allocation)
    if not allocation_path.is_file():
        raise FileNotFoundError(allocation_path)
    allocation_sha256 = sha256_file(allocation_path)

    registry_path = resolve_path(args.registry) if args.registry is not None else None
    registry = load_registry_if_available(args.registry)
    parent_row = parent_from_registry(registry, args.parent_candidate_id)
    parent_graph_path, parent_graph_sha256, parent_notes = resolve_parent_graph(
        args.parent_graph,
        parent_row,
    )
    if args.parent_candidate_id and parent_row is None:
        parent_notes.append(
            f"Parent candidate {args.parent_candidate_id} was not found in registry."
        )

    manifest = build_manifest(
        args=args,
        allocation_path=args.allocation,
        allocation_sha256=allocation_sha256,
        registry_path=args.registry if registry_path is not None else None,
        parent_row=parent_row,
        parent_graph_path=parent_graph_path,
        parent_graph_sha256=parent_graph_sha256,
        parent_notes=parent_notes,
    )

    for directory in dirs:
        directory.mkdir(parents=True, exist_ok=True)
    manifest_path.write_text(
        json.dumps(manifest, indent=2, sort_keys=True) + "\n",
        encoding="utf-8",
    )
    readme_path.write_text(readme_text(args, manifest), encoding="utf-8")

    print(f"candidate_id={args.candidate_id}")
    print(f"candidate_root={candidate_root}")
    print(f"manifest={manifest_path}")
    print(f"readme={readme_path}")
    print(f"allocation_sha256={allocation_sha256}")
    print(f"parent_graph_path={parent_graph_path}")
    print(f"parent_graph_sha256={parent_graph_sha256}")
    print("status=planned_not_generated")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
